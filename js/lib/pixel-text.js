function escapeAttr(s) {
  return String(s ?? '')
    .replace(/&/g, '&amp;')
    .replace(/"/g, '&quot;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;');
}

export function normalizePixelText(s) {
  return String(s ?? '')
    .replace(/[“”]/g, '"')
    .replace(/[‘’]/g, "'")
    .replace(/[‐‑‒–—]/g, '-')
    .replace(/[…]/g, '...')
    .replace(/[·•]/g, ' ')
    .replace(/\s+/g, ' ')
    .trim()
    .toUpperCase();
}

export function pixelTextWidth(text, scale = 4, charGap = 1) {
  return pixelTextMetrics(text, { scale, charGap }).width;
}

export function pixelTextMetrics(text, opts = {}) {
  const scale = Math.max(0.25, opts.scale || 4);
  const charGap = opts.charGap ?? 1;
  const font = opts.font === 'tiny' ? 'tiny' : 'standard';
  const glyphW = font === 'tiny' ? 3 : 5;
  const glyphH = font === 'tiny' ? 5 : 7;
  const chars = normalizePixelText(text).split('');
  const width = chars.length
    ? chars.length * glyphW * scale + Math.max(0, chars.length - 1) * charGap * scale
    : 0;
  return { width, height: glyphH * scale, glyphW, glyphH };
}

export function pixelTextHeight(scale = 4, font = 'standard') {
  return (font === 'tiny' ? 5 : 7) * scale;
}

export function wrapPixelText(text, maxWidth, scale = 4, maxLines = 3, charGap = 1, font = 'standard') {
  const normalized = normalizePixelText(text);
  if (!normalized) return [];
  const words = normalized.split(/\s+/).filter(Boolean);
  const lines = [];
  let current = '';
  for (const word of words) {
    const next = current ? `${current} ${word}` : word;
    if (pixelTextMetrics(next, { scale, charGap, font }).width <= maxWidth || !current) {
      current = next;
      continue;
    }
    lines.push(current);
    current = word;
    if (lines.length >= maxLines) break;
  }
  if (current && lines.length < maxLines) lines.push(current);
  if (lines.length === maxLines && words.length) {
    const consumed = lines.join(' ').split(/\s+/).length;
    if (consumed < words.length) {
      let last = lines[lines.length - 1];
      while (last.length > 4 && pixelTextMetrics(`${last}...`, { scale, charGap, font }).width > maxWidth) {
        last = last.slice(0, -1).trim();
      }
      lines[lines.length - 1] = `${last}...`;
    }
  }
  return lines;
}

export function pixelText(text, x, y, opts = {}) {
  const scale = Math.max(0.25, opts.scale || 4);
  const charGap = opts.charGap ?? 1;
  const fill = opts.fill || '#fff7e2';
  const font = opts.font === 'tiny' ? 'tiny' : 'standard';
  const glyphs = font === 'tiny' ? TINY_GLYPHS : GLYPHS;
  const fallback = font === 'tiny' ? TINY_FALLBACK : FALLBACK;
  const glyphW = font === 'tiny' ? 3 : 5;
  const opacityAttr = opts.opacity == null ? '' : ` fill-opacity="${opts.opacity}"`;
  const source = String(text ?? '');
  const normalized = normalizePixelText(source);
  const out = [
    `<g data-pixel-text="1" data-text="${escapeAttr(source)}" aria-label="${escapeAttr(source)}" role="img">`
  ];
  let cursorX = x;
  for (const ch of normalized) {
    const glyph = glyphs[ch] || fallback;
    for (let row = 0; row < glyph.length; row++) {
      const bits = glyph[row];
      for (let col = 0; col < bits.length; col++) {
        if (bits[col] !== '1') continue;
        out.push(
          `<rect x="${cursorX + col * scale}" y="${y + row * scale}" width="${scale}" height="${scale}" fill="${fill}"${opacityAttr}/>`
        );
      }
    }
    cursorX += (glyphW + charGap) * scale;
  }
  out.push('</g>');
  return out.join('');
}

export function pixelTextLines(lines, x, y, opts = {}) {
  const scale = opts.scale || 4;
  const lineGap = opts.lineGap ?? scale * 3;
  const lineHeight = pixelTextHeight(scale, opts.font) + lineGap;
  return lines.map((line, i) => pixelText(line, x, y + i * lineHeight, opts)).join('');
}
